package cs3500.pa04.model.types;

/**
 * Represents an enumeration type for a game status (win, lose, draw)
 */
public enum GameResult {
  /**
   * represents when a player has won the game
   */
  WIN,

  /**
   * represents when a player has lost the game
   */
  LOSE,

  /**
   * represents when a player draws in a game
   */
  DRAW,
}
